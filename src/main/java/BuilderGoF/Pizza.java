package BuilderGoF;

public class Pizza {
    private String ciasto;
    private String ser;
    private String mieso;
    private String warzywa;
    private String owoce;
    private String sos;
    private String dodatki;

    public String getCiasto() {
        return ciasto;
    }

    public void setCiasto(String ciasto) {
        this.ciasto = ciasto;
    }

    public String getSer() {
        return ser;
    }

    public void setSer(String ser) {
        this.ser = ser;
    }

    public String getMieso() {
        return mieso;
    }

    public void setMieso(String mieso) {
        this.mieso = mieso;
    }

    public String getWarzywa() {
        return warzywa;
    }

    public void setWarzywa(String warzywa) {
        this.warzywa = warzywa;
    }

    public String getOwoce() {
        return owoce;
    }

    public void setOwoce(String owoce) {
        this.owoce = owoce;
    }

    public String getSos() {
        return sos;
    }

    public void setSos(String sos) {
        this.sos = sos;
    }

    public String getDodatki() {
        return dodatki;
    }

    public void setDodatki(String dodatki) {
        this.dodatki = dodatki;
    }
}
